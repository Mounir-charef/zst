import { cn } from '@mono/util';
import { NextIntlClientProvider } from 'next-intl';
import { getMessages, getTimeZone } from 'next-intl/server';
import { Inter } from 'next/font/google';
import { Toaster } from 'sonner';
import { availableLocalesMap, defaultLocale } from '../../../i18n/locales';
import '../../global.css';
import { Providers } from '../../components/Providers';
import NavBar from '../../components/navbar/NavBar';

export const metadata = {
  title: 'Welcome to admin',
  description: 'Generated by alger',
};

const inter = Inter({ subsets: ['latin'] });

export default async function RootLayout({
  children,
  params: { locale },
}: {
  children: React.ReactNode;
  params: { locale: string };
}) {
  const messages = await getMessages();
  const timezone = await getTimeZone();
  const { langDir, hrefLang } = availableLocalesMap[locale] || defaultLocale;
  return (
    <html
      lang={hrefLang}
      dir={langDir}
      className="h-full"
      suppressHydrationWarning
    >
      <body
        className={cn(
          'text-foreground bg-background h-full font-sans antialiased',
          inter.className,
        )}
      >
        <Providers>
          <NextIntlClientProvider messages={messages} timeZone={timezone}>
            <main className="flex h-full w-full flex-col">
              <NavBar />
              <div className="mx-auto w-full max-w-7xl flex-grow flex-col py-12 sm:py-6">
                {children}
              </div>
            </main>
            <Toaster richColors closeButton position="top-right" />
          </NextIntlClientProvider>
        </Providers>
      </body>
    </html>
  );
}
